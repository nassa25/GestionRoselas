    private void btnConnexionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConnexionMouseClicked
        Connecter con = new Connecter();
        Statement stm;
        ResultSet Rs;
        String nomUser = txt_username.getText();
        String pw = String.valueOf(txtPassword.getText().toString());
        try {

            stm = con.obtenirConnexion().createStatement();
            Rs = stm.executeQuery("select * from login where nomUser='" + txt_username.getText() + "'");
            while (Rs.next()) {
                String nomL = Rs.getString("nomUser");
                String passeL = Rs.getString("mdpUser");
                if (nomUser.equals(nomL) && pw.equals(passeL)) {
                    new Admin().setVisible(true);
                    this.setVisible(false);
                    txt_username.setText("");
                    txtPassword.setText("");

                } else {
                    txt_username.setText("");
                    txtPassword.setText("");

                    JOptionPane.showMessageDialog(null, "Nom d'utilisateur ou mot de passe erron√©e!");
                }
            }

        } catch (SQLException e) {
            System.err.println(e);
        }


    }//GEN-LAST:event_btnConnexionMouseClicked

