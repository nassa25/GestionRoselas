    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String dateSortie = ((JTextField) dt_sortie.getDateEditor().getUiComponent()).getText();
        String idSortie = "0";
        String nomCli = cbx_clientSortie.getSelectedItem().toString();
        String nomProd = cbx_produitSortie.getSelectedItem().toString();
        String qte = txt_sortieQte.getText();
        if (dateSortie.equals("") && nomCli.equals("") && nomProd.equals("") && qte.equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez completer tous les champs svp!");
        } else {
            String requete = "INSERT INTO sortie(idSortie,dateSortie,nomClient,designProduit, qteSortie) VALUES ('" + idSortie + "','" + dateSortie + "','" + nomCli + "','" + nomProd + "','" + qte + "')";

            try {
                stm = con.obtenirConnexion().createStatement();
                stm.executeUpdate(requete);
                JOptionPane.showMessageDialog(null, "Ajoute avec succes");
                stm1.executeUpdate("UPDATE produit SET stock= (stock - '" + qte + "') WHERE designProduit='" + nomProd + "'");
                ((JTextField) dt_sortie.getDateEditor().getUiComponent()).setText("");
                cbx_clientSortie.getSelectedItem().toString();
                cbx_produitSortie.getSelectedItem().toString();
                txt_sortieQte.setText("");

                this.setVisible(false);
            } catch (HeadlessException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

