    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String dateEntree = ((JTextField) dt_entreeModifier.getDateEditor().getUiComponent()).getText();
        String idEntre = txt_modifierEntreeId.getText();
        String nomCli = cbx_clientEntreeModifier.getSelectedItem().toString();
        String montantEntree = txt_modifierEntreePai.getText();
        try {
            stm = con.obtenirConnexion().createStatement();
            if (JOptionPane.showConfirmDialog(null, "Vous voulez vraiment modifier", "modification",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {

                stm.executeUpdate("UPDATE entree SET dateEntree='" + dateEntree + "',nomClient='" + nomCli + "',montantEntree='" + montantEntree + "' WHERE idEntree=" + idEntre);
                ((JTextField) dt_entreeModifier.getDateEditor().getUiComponent()).setText("");
                cbx_clientEntreeModifier.getSelectedItem().toString();
                txt_modifierEntreePai.setText("");
                txt_modifierEntreeId.setText("");
                this.setVisible(false);
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Erreur de modification !" + e.getMessage());
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

