    private void lbl_produitRechercheMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_produitRechercheMouseClicked
        try {
            model.setRowCount(0);
            {
                Rs = stm.executeQuery("SELECT * from produit WHERE numProduit LIKE '%" + txt_rechercheProduit.getText() + "%' OR designProduit LIKE '%" + txt_rechercheProduit.getText() + "%' OR pu LIKE '%" + txt_rechercheProduit.getText() + "%' OR stock LIKE '%" + txt_rechercheProduit.getText() + "%' ");
                txt_rechercheProduit.setText("");
            }
            while (Rs.next()) {
                Object[] produit = {Rs.getString("idProduit"), Rs.getString("numProduit"), Rs.getString("designProduit"), Rs.getString("pu"), Rs.getString("stock")};
                model.addRow(produit);
                if (model.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(null, "Aucun produit correspond a ce numero !");
                }
            }
        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_lbl_produitRechercheMouseClicked

