    private void btn_produitSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_produitSupprimerActionPerformed
        TableModel model1 = tblProduit.getModel();
        int indexs[] = tblProduit.getSelectedRows();
        String id, num, design, pu, stock;
        Object[] row = new Object[1];

        for (int i = 0; i < indexs.length; i++) {
            id = (String) model1.getValueAt(indexs[i], 0);
            num = (String) model1.getValueAt(indexs[i], 1);
            design = (String) model1.getValueAt(indexs[i], 2);
            pu = (String) model1.getValueAt(indexs[i], 3);
            stock = (String) model1.getValueAt(indexs[i], 4);
            try {
                stm = con.obtenirConnexion().createStatement();
                if (JOptionPane.showConfirmDialog(null, "Vous voulez vraiment supprimer", "suppression",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
                    if (id.length() != 0) {
                        stm.executeUpdate("DELETE from produit WHERE idProduit=" + id);
                        affiche();
                    } else {
                        JOptionPane.showConfirmDialog(null, "Veuillez remplir le champ id");
                    }
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Erreur de suppression !" + e.getMessage());
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btn_produitSupprimerActionPerformed

