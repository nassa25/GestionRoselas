    private void btn_clientSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_clientSupprimerActionPerformed
        TableModel model1 = tblClient.getModel();
        int indexs[] = tblClient.getSelectedRows();
        String id, nom, adr, contact;
        Object[] row = new Object[1];

        for (int i = 0; i < indexs.length; i++) {
            id = (String) model1.getValueAt(indexs[i], 0);
            nom = (String) model1.getValueAt(indexs[i], 1);
            adr = (String) model1.getValueAt(indexs[i], 2);
            contact = (String) model1.getValueAt(indexs[i], 3);
            try {
                stm = con.obtenirConnexion().createStatement();
                if (JOptionPane.showConfirmDialog(null, "Vous voulez vraiment supprimer", "suppression",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
                    if (id.length() != 0) {
                        stm.executeUpdate("DELETE from client WHERE idClient=" + id);
                        affiche();
                    } else {
                        JOptionPane.showConfirmDialog(null, "Veuillez remplir le champ id");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erreur de suppression !" + e.getMessage());
                System.err.println(e);
            }
        }

    }//GEN-LAST:event_btn_clientSupprimerActionPerformed

