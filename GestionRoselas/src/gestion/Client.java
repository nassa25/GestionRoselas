    private void lbl_clientRechercheMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_clientRechercheMouseClicked
        try {
            model.setRowCount(0);
            {
                Rs = stm.executeQuery("SELECT * from client WHERE nomClient LIKE '%" + txt_rechercheClient.getText() + "%' OR addresse LIKE '%" + txt_rechercheClient.getText() + "%' ");
                txt_rechercheClient.setText("");
            }
            while (Rs.next()) {
                Object[] client = {Rs.getString("idClient"), Rs.getString("nomClient"), Rs.getString("addresse"), Rs.getString("contact")};
                model.addRow(client);
                if (model.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(null, "Aucun client correspond!");
                }
            }
        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_lbl_clientRechercheMouseClicked

